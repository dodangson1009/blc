export default async function handler(req, res) {
  const { id } = req.query
  if (!id) return res.status(400).json({ error: 'Coin ID is required' })

  try {
    const url = getCoinGeckoUrl(`/coins/${id}`, {
      localization: false,
      tickers: false,
      market_data: true,
      community_data: false,
      developer_data: false,
      sparkline: false,
    })

    const response = await fetchWithRetry(url)
    const data = await response.json()

    if (data.error || !data.id) {
      console.log(`CoinGecko API unavailable for ${id}, using mock data`)
      const mockData = MOCK_COIN_DETAILS[id]
      if (mockData) {
        return res.status(200).json({ data: mockData, mock: true })
      }
      return res.status(404).json({ error: 'Coin not found' })
    }

    res.status(200).json({ data })
  } catch (error) {
    console.error('getCoinDetail error, using mock data:', error.message)
    const mockData = MOCK_COIN_DETAILS[id]
    if (mockData) {
      return res.status(200).json({ data: mockData, mock: true })
    }
    res.status(500).json({ error: 'Failed to load coin data' })
  }
}
